#!/usr/bin/env node
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ENDPOINT = "https://api.sr.se/api/v2/channels?format=json&pagination=false";
const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const PACKAGE_DIR = path.resolve(SCRIPT_DIR, "..");
const OUTPUT_PATH = path.join(PACKAGE_DIR, "sr-channels.ts");

function parseArgs(argv) {
  const args = { mode: "check", sample: null };
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === "--write") args.mode = "write";
    else if (arg === "--check") args.mode = "check";
    else if (arg === "--sample") args.sample = argv[++i];
    else throw new Error(`Unknown argument: ${arg}`);
  }
  return args;
}

function slugify(value) {
  return value
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "") || "channel";
}

function jsonString(value) {
  return JSON.stringify(value);
}

function validateChannel(value) {
  return value && typeof value === "object" && typeof value.id === "number" && typeof value.name === "string";
}

async function loadData(samplePath) {
  if (samplePath) {
    return JSON.parse(await readFile(path.resolve(samplePath), "utf8"));
  }

  const response = await fetch(ENDPOINT, { headers: { accept: "application/json" } });
  if (!response.ok) {
    throw new Error(`Failed to fetch SR channels: ${response.status} ${response.statusText}`);
  }
  return response.json();
}

function normalizeChannels(data) {
  if (!data || typeof data !== "object" || !Array.isArray(data.channels)) {
    throw new Error("Unexpected SR response: missing channels array");
  }

  const channels = data.channels
    .filter(validateChannel)
    .filter((channel) => typeof channel.liveaudio?.url === "string" && /^https?:\/\//.test(channel.liveaudio.url))
    .sort((a, b) => String(a.name).localeCompare(String(b.name), "sv"));

  const slugCounts = new Map();
  const baseSlugs = channels.map((channel) => {
    const base = `channel-sr-${slugify(channel.name)}`;
    slugCounts.set(base, (slugCounts.get(base) ?? 0) + 1);
    return base;
  });

  return channels.map((channel, index) => {
    const base = baseSlugs[index];
    const id = slugCounts.get(base) > 1 ? `${base}-${channel.id}` : base;
    const tagline = typeof channel.tagline === "string" && channel.tagline.trim() ? channel.tagline.trim() : "Sveriges Radio live channel";
    return {
      id,
      name: channel.name,
      description: `Sveriges Radio — ${tagline}`,
      url: channel.liveaudio.url,
      country: "SE",
      source: "sr",
      sourceId: String(channel.id),
      website: typeof channel.siteurl === "string" ? channel.siteurl : undefined,
    };
  });
}

function render(channels) {
  const lines = [
    "// Generated by scripts/update-sr-channels.mjs from Sveriges Radio API v2. Do not edit by hand.",
    `// Source: ${ENDPOINT}`,
    `// Channels with liveaudio.url: ${channels.length}`,
    "",
    'import type { Channel } from "./channels.ts";',
    "",
    "export const SR_CHANNELS: Channel[] = [",
  ];

  for (const channel of channels) {
    lines.push("  {");
    lines.push(`    id: ${jsonString(channel.id)},`);
    lines.push(`    name: ${jsonString(channel.name)},`);
    lines.push(`    description: ${jsonString(channel.description)},`);
    lines.push(`    url: ${jsonString(channel.url)},`);
    lines.push(`    country: "SE",`);
    lines.push(`    source: "sr",`);
    lines.push(`    sourceId: ${jsonString(channel.sourceId)},`);
    if (channel.website) lines.push(`    website: ${jsonString(channel.website)},`);
    lines.push("  },");
  }

  lines.push("];", "");
  return lines.join("\n");
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const data = await loadData(args.sample);
  const channels = normalizeChannels(data);
  const output = render(channels);

  if (args.mode === "write") {
    await writeFile(OUTPUT_PATH, output, "utf8");
    console.log(`Wrote ${channels.length} SR channels to ${path.relative(process.cwd(), OUTPUT_PATH)}`);
    return;
  }

  let current = "";
  try {
    current = await readFile(OUTPUT_PATH, "utf8");
  } catch {
    // Missing file is a check failure below.
  }
  if (current !== output) {
    throw new Error("sr-channels.ts is out of date. Run npm run update:sr-channels.");
  }
  console.log(`sr-channels.ts up to date (${channels.length} channels)`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});
